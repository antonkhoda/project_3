$('#dropdown_batton_1').click(function() {
  if ( $( "#dropdown_content_1" ).is( ":hidden" ) ) {
      $( "#dropdown_content_1" ).show( "slow" );
      $("#dropdown_batton_1").toggleClass("screen_9_dropdown_batton_pointer_spin");
  } else {
  $( "#dropdown_content_1" ).fadeOut("slow");
  $("#dropdown_batton_1").toggleClass("screen_9_dropdown_batton_pointer_spin");
  }
});

